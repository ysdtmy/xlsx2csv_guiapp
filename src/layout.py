    def convert(self):

            try:
                    self.model

            except:
                    result = False
                    self.showdialog(result, Errormsg="XLSX file is not selected")
                    return False

            sepdict = {
                    'comma': [',', '.csv']
                    , 'tab': ['\t', '.tsv']
                    , 'space': [' ', '.txt']
                    , "pipe \"|\"": ['|', '.txt']
            }

            self.outputdict = {}
            self.checked = []
            self.model.persistentIndexList()
            self.modelindex = self.model.persistentIndexList()
            index = self.modelindex[0]
            itemdict = self.model.itemData(index)
            radiocheck = self.radioButton.isChecked()

            self.outputdict['worksheet'] = itemdict[0]
            self.outputdict['headeronly'] = radiocheck
            self.outputdict['sep'] = sepdict[self.comboBox.currentText()]
            self.outputdict['encoding'] = self.comboBox_2.currentText()
            self.outputdict['output'] = self.lineEdit.text()

            self.result = True

            if self.outputdict['output'] == '':
                    result = False
                    self.showdialog(result, Errormsg="Sheetname is empty!")
                    return False

            try:
                    xlsx2tsv.sheet2tsv(
                            self.filename
                            , self.outputdict['worksheet']
                            , self.outputdict['output'] + self.outputdict['sep'][1]
                            , sep=self.outputdict['sep'][0]
                            , skip=self.outputdict['headeronly']
                            , encoding=self.outputdict['encoding']
                    )

            except Exception as e:
                    self.result = False
                    self.Errormsg = str(e)

            self.showdialog(self.result)

